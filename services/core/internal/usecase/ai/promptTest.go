package ai

import (
	"fmt"
	"strings"
)

func buildTestPrompt(input GenerateTestInput) string {

	var sb strings.Builder

	for i, t := range input.Tasks {
		fmt.Fprintf(&sb, "%d. %s - %s\n", i+1, t.Title, t.Description)
	}

	return fmt.Sprintf(`
Ты Senior преподаватель и эксперт по оценке профессиональных навыков.

Твоя задача — создать итоговый теоретический тест по материалам учебного плана.

ВАЖНО:
Тест должен проверять только профессиональные знания и навыки сотрудника.
НЕ создавай вопросы про сам процесс обучения, прохождение теста, правила тестирования или систему оценки.

ИГНОРИРУЙ любые задачи, связанные с:
- прохождением тестирования;
- сдачей теста;
- получением баллов;
- порогом успешного прохождения;
- подтверждением навыка;
- отправкой результатов;
- завершением плана.

Например, вопросы вида:
"Какой минимальный процент нужен для прохождения теста?"
"Сколько вопросов нужно ответить правильно?"
"Что происходит после успешной сдачи теста?"
— ЗАПРЕЩЕНЫ.

Вопросы должны быть только по изучаемой теме.

План обучения:

Название:
%s

Описание:
%s

Учебные задачи:
%s


Требования:

1. Создай РОВНО 10 вопросов.

2. Каждый вопрос должен проверять знания по теме обучения.

3. Каждый вопрос должен иметь ровно 4 варианта ответа.

4. Только один вариант ответа является правильным.

5. Правильный ответ укажи только одной буквой:
A, B, C или D.

6. Не создавай вопросы про:
- тест;
- экзамен;
- оценки;
- процент прохождения;
- структуру приложения SkillTracker;
- процесс обучения.

7. Не используй markdown.

8. Не добавляй никаких пояснений.

9. Верни ТОЛЬКО валидный JSON.


Формат ответа:

{
    "questions":[
        {
            "question":"",
            "option_a":"",
            "option_b":"",
            "option_c":"",
            "option_d":"",
            "correct_option":""
        }
    ]
}
`,
		input.PlanTitle,
		input.PlanDescription,
		sb.String(),
	)
}
